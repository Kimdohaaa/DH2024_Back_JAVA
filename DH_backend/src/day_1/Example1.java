package day_1;

public class Example1 {

	public static void main(String[] args) {
		// p.45 변수 선업 및 사용
		int level1;  // 정수형 변수 level1 선언
		level1 = 10; // 변수 level1에 10 대입
		
		System.out.println(level1);	// level1 출력
		
		
		// p.46 변수 초기화
		int level2 =10; // 정수형 변수 level2를 선언함과 동시에 초기화로 값 지정
		
		System.out.println(level2);	// 변수 level2 출력
		
		// 예제 : 변수 age를 선언하고 본인 나이를 대입하라
		int age = 24;
		
		System.out.println(age);
		
		// 예제 : ing형 변수 year을 2018로 초기화하시오.
		int year = 2018;
		System.out.println(year);
	}

}
